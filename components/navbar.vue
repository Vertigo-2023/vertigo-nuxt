<template>
  <nav>
    <div class="wrapper">
      <ul class="nav-links">
        <li :class="isFormationSelected?'selected':''" @click="this.$router.push('formation')"><a href="#" >Formation</a></li>
        <li :class="isOutilSelected?'selected':''"  @click="this.$router.push('outil')"><a href="#">Outil</a></li>
      </ul>
      <label for="menu-btn" class="btn menu-btn"><i class="fas fa-bars"></i></label>
    </div>
  </nav>
</template>

<script lang="ts">
export default defineComponent({
  name:"navbar",
  data: () => {
    return{
      sideVisible: false as boolean,
      isOutilSelected:false as boolean,
      isFormationSelected:false as boolean,
    }
  },
  methods:{
    onRouteChange(){
      if(this.$route.name === 'outil'){
        this.isOutilSelected = true;
        this.isFormationSelected = false;
      }else if(this.$route.name ==='formation'){
        this.isFormationSelected = true;
        this.isOutilSelected = false;
      }
    }
  },
  watch: {
    $route: 'onRouteChange',
  },
  mounted() {
    this.onRouteChange();
  },
  beforeRouteUpdate(to, from, next) {
    if(next.name === 'outil'){
      this.isOutilSelected = true;
    }else if(next.name ==='formation'){
      this.isFormationSelected = true;
    }
    next();
  },
})
</script>

